/**
 * WhatsApp Templates for Ksheermitra
 */
class WhatsAppTemplates {
  // Helper method to get product emoji
  static getProductEmoji(name) {
    const lowerName = name.toLowerCase();
    if (lowerName.includes('milk')) return '🥛';
    if (lowerName.includes('curd') || lowerName.includes('dahi')) return '🥣';
    if (lowerName.includes('butter') || lowerName.includes('ghee')) return '🧈';
    if (lowerName.includes('lassi')) return '🥤';
    if (lowerName.includes('paneer')) return '🧀';
    return '📦';
  }

  // Helper method to format frequency
  static formatFrequency(frequency) {
    const freqMap = {
      'daily': 'Daily',
      'alternate': 'Alternate Days',
      'weekly': 'Weekly',
      'monthly': 'Monthly'
    };
    return freqMap[frequency] || frequency;
  }

  // OTP Verification
  static generateOTPMessage(otp, expiryMinutes = 10, userName = null) {
    const greeting = userName ? `Hi *${userName}*` : 'Hello';
    return `🥛 *KSHEERMITRA*

${greeting}!

Your verification code: *${otp}*

Valid for ${expiryMinutes} minutes

Need help? Reply HELP`;
  }

  // Welcome Message
  static generateWelcomeMessage(userName, phone) {
    return `🥛 *Welcome to Ksheermitra!*

Hi ${userName},

Your account is now active!

📱 ${phone}
✅ 100% Pure Farm-Fresh Milk
🚚 Doorstep Delivery
💳 Flexible Payments

Get started:
1. Choose products
2. Set delivery schedule
3. Enjoy daily fresh milk

Need help? Reply HELP`;
  }

  // Subscription Created
  static generateSubscriptionCreated(data) {
    const {
      customerName,
      products,
      frequency,
      startDate,
      totalPerDelivery,
      subscriptionId,
      deliveryTime = '6:00-8:00 AM'
    } = data;

    const productList = products.map(p =>
      `${this.getProductEmoji(p.name)} ${p.name} - ${p.quantity} ${p.unit}`
    ).join('\n');

    return `✅ *Subscription Confirmed!*

Hi ${customerName},

🆔 #${subscriptionId}

*Your Products:*
${productList}

📅 Frequency: ${this.formatFrequency(frequency)}
🕐 Delivery Time: ${deliveryTime}
📆 Starts: ${startDate}
💰 Per Delivery: ₹${totalPerDelivery}

Your first delivery will arrive soon!

Track deliveries in app 📱`;
  }

  // Delivery Confirmed
  static generateDeliveryConfirmed(data) {
    const {
      customerName,
      deliveryDate,
      products,
      amount,
      deliveryBoyName
    } = data;

    const productList = products.map(p =>
      `✓ ${p.name}: ${p.quantity} ${p.unit}`
    ).join('\n');

    return `✅ *Delivery Completed*

Hi ${customerName},

📅 Date: ${deliveryDate}

*Delivered:*
${productList}

💰 Amount: ₹${amount}
🚚 By: ${deliveryBoyName}

Thank you for your payment! 🙏

Any issues? Reply to this message.`;
  }

  // Delivery Missed
  static generateDeliveryMissed(data) {
    const {
      customerName,
      deliveryDate,
      reason = 'Customer not available'
    } = data;

    return `⚠️ *Delivery Missed*

Hi ${customerName},

📅 Date: ${deliveryDate}
❌ Reason: ${reason}

We'll try to deliver tomorrow or you can:
• Contact us to reschedule
• Update delivery instructions in app

Sorry for the inconvenience! 🙏`;
  }

  // Monthly Invoice
  static generateMonthlyInvoice(data) {
    const {
      customerName,
      month,
      totalDelivered,
      totalMissed,
      totalCollected,
      deliverySummary,
      pdfUrl,
      moreCount = ''
    } = data;

    return `📊 *Monthly Invoice - ${month}*

Hi ${customerName},

✅ Delivered: ${totalDelivered}
❌ Missed: ${totalMissed}
📦 Total: ${totalDelivered + totalMissed}

💰 *Total Collected: ₹${totalCollected.toFixed(2)}*
━━━━━━━━━━━━━━━━

*Top Deliveries:*
${deliverySummary}${moreCount}

🧾 Full Invoice: ${pdfUrl}

Generated by KsheerMitra System`;
  }

  // Area Assignment Notification to Delivery Boy
  static generateAreaAssignedNotification(data) {
    const {
      deliveryBoyName,
      areaName,
      totalCustomers,
      mapLink
    } = data;

    return `🗺️ *New Area Assigned!*

Hi ${deliveryBoyName},

You have been assigned to:
📍 *${areaName}*

👥 Customers: ${totalCustomers}
🗺️ View Area: ${mapLink}

Your deliveries will start from tomorrow.
Check your app for customer details and routes.

All the best! 💪`;
  }

  // Subscription Details with Products
  static generateSubscriptionDetailsMessage(data) {
    const {
      customerName,
      subscriptions,
      deliveryDate
    } = data;

    const subscriptionList = subscriptions.map(sub => {
      const products = sub.products.map(p =>
        `  • ${p.name}: ${p.quantity} ${p.unit} @ ₹${p.rate}`
      ).join('\n');
      return `*${sub.name}*\n${products}\n  Subtotal: ₹${sub.total}`;
    }).join('\n\n');

    const totalAmount = subscriptions.reduce((sum, sub) => sum + sub.total, 0);

    return `📋 *Today's Delivery Details*

Hi ${customerName},

📅 Date: ${deliveryDate}

*Your Subscriptions:*
${subscriptionList}

━━━━━━━━━━━━━━━━
💰 *Total Today: ₹${totalAmount.toFixed(2)}*
━━━━━━━━━━━━━━━━

Your delivery will arrive between 6:00-8:00 AM

Thank you! 🥛`;
  }

  // Wrapper methods for backward compatibility
  static generateSubscriptionCreatedMessage(data) {
    return this.generateSubscriptionCreated(data);
  }

  static generateDeliveryConfirmationMessage(data) {
    return this.generateDeliveryConfirmed(data);
  }

  static generateDeliveryMissedMessage(data) {
    return this.generateDeliveryMissed(data);
  }

  // Daily Invoice Message for Admin
  static generateInvoiceMessage(data) {
    const {
      customerName,
      invoiceNumber,
      totalDeliveries,
      totalAmount,
      date,
      deliveries
    } = data;

    let deliveryList = '';
    if (deliveries && deliveries.length > 0) {
      deliveryList = deliveries.slice(0, 10).map((d, i) =>
        `${i + 1}. ${d.customerName} - ₹${d.amount}`
      ).join('\n');
      if (deliveries.length > 10) {
        deliveryList += `\n... and ${deliveries.length - 10} more`;
      }
      deliveryList = `\n*Deliveries:*\n${deliveryList}\n`;
    }

    return `🧾 *Daily Invoice Generated*

📋 Invoice: *${invoiceNumber}*
📅 Date: ${date}
🚚 Delivery Boy: *${customerName}*

✅ Total Deliveries: ${totalDeliveries}
💰 Total Amount: *₹${totalAmount}*
${deliveryList}
━━━━━━━━━━━━━━━━
📄 PDF invoice attached below

Generated by KsheerMitra System`;
  }
}

module.exports = WhatsAppTemplates;
